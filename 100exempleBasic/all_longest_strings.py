def allLongestStrings(s):
    max = 0
    l = []
    for i in range(len(s)):
        len(s[i])
        if len(s[i]) >= max:
            max = len(s[i])
    for i in range(len(s)):
        len(s[i])
        if len(s[i]) >= max:
            max = len(s[i])
            l.append(s[i])
    print(l)
